/**
 * Sets a quaternion to represent the shortest rotation from one
 * vector to another.
 *
 * Both vectors are assumed to be unit length.
 *
 * @param {quat} out the receiving quaternion.
 * @param {ReadonlyVec3} a the initial vector
 * @param {ReadonlyVec3} b the destination vector
 * @returns {quat} out
 */
export const rotationTo = (function () {
  let tmpvec3 = vec3Math.create();
  let xUnitVec3 = vec3Math.fromValues(1, 0, 0);
  let yUnitVec3 = vec3Math.fromValues(0, 1, 0);

  return function (out: quat, a: ReadonlyVec3, b: ReadonlyVec3): quat {
    let dot = vec3Math.dot(a, b);
    if (dot < -0.999999) {
      vec3Math.cross(tmpvec3, xUnitVec3, a);
      if (vec3Math.len(tmpvec3) < 0.000001) vec3Math.cross(tmpvec3, yUnitVec3, a);
      vec3Math.normalize(tmpvec3, tmpvec3);
      setAxisAngle(out, tmpvec3, Math.PI);
      return out as Quat;
    } else if (dot > 0.999999) {
      out[0] = 0;
      out[1] = 0;
      out[2] = 0;
      out[3] = 1;
      return out as Quat;
    } else {
      vec3Math.cross(tmpvec3, a, b);
      out[0] = tmpvec3[0];
      out[1] = tmpvec3[1];
      out[2] = tmpvec3[2];
      out[3] = 1 + dot;
      return normalize(out, out);
    }
  };
})();

/**
 * Performs a spherical linear interpolation with two control points
 *
 * @param {quat} out the receiving quaternion
 * @param {ReadonlyQuat} a the first operand
 * @param {ReadonlyQuat} b the second operand
 * @param {ReadonlyQuat} c the third operand
 * @param {ReadonlyQuat} d the fourth operand
 * @param {Number} t interpolation amount, in the range [0-1], between the two inputs
 * @returns {quat} out
 */
export const sqlerp = (function () {
  let temp1 = create();
  let temp2 = create();

  return function (
    out: quat,
    a: ReadonlyQuat,
    b: ReadonlyQuat,
    c: ReadonlyQuat,
    d: ReadonlyQuat,
    t: number
  ): quat {
    slerp(temp1, a, d, t);
    slerp(temp2, b, c, t);
    slerp(out, temp1, temp2, 2 * t * (1 - t));

    return out as Quat;
  };
})();

/**
 * Sets the specified quaternion with values corresponding to the given
 * axes. Each axis is a vec3 and is expected to be unit length and
 * perpendicular to all other specified axes.
 *
 * @param {ReadonlyVec3} view  the vector representing the viewing direction
 * @param {ReadonlyVec3} right the vector representing the local "right" direction
 * @param {ReadonlyVec3} up    the vector representing the local "up" direction
 * @returns {quat} out
 */
export const setAxes = (function () {
  let matr = mat3.create();

  return function (out: quat, view: ReadonlyVec3, right: ReadonlyVec3, up: ReadonlyVec3): quat {
    matr[0] = right[0];
    matr[3] = right[1];
    matr[6] = right[2];

    matr[1] = up[0];
    matr[4] = up[1];
    matr[7] = up[2];

    matr[2] = -view[0];
    matr[5] = -view[1];
    matr[8] = -view[2];

    return normalize(out, fromMat3(out, matr));
  };
})();
